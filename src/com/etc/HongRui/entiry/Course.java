package com.etc.HongRui.entiry;



public class Course {
	public String cid;//课程ID
	public String cname;//课程名
	public int clength;//时长
	public float cprice;//价格
	public String cintroduce;//简介
	public int cbase;//基础/进阶
	public String cstatus;//审核状态 
	public String ctype;//课程名类型
	public String cctype;//课程类型
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getClength() {
		return clength;
	}
	public void setClength(int clength) {
		this.clength = clength;
	}
	
	public float getCprice() {
		return cprice;
	}
	public void setCprice(float cprice) {
		this.cprice = cprice;
	}
	public String getCintroduce() {
		return cintroduce;
	}
	public void setCintroduce(String cintroduce) {
		this.cintroduce = cintroduce;
	}
	public int getCbase() {
		return cbase;
	}
	public void setCbase(int cbase) {
		this.cbase = cbase;
	}
	public String getCstatus() {
		return cstatus;
	}
	public void setCstatus(String cstatus) {
		this.cstatus = cstatus;
	}
	public String getCtype() {
		return ctype;
	}
	public void setCtype(String ctype) {
		this.ctype = ctype;
	}
	public String getCctype() {
		return cctype;
	}
	public void setCctype(String cctype) {
		this.cctype = cctype;
	}
	
	
}
